// Add caching for translations
const translationCache = new Map();

/**
 * Translates text from one language to another
 * @param {string} text - Text to translate
 * @param {string} from - Source language code
 * @param {string} to - Target language code
 * @returns {string} - Translated text or original if translation not found
 */
function translate(text, from, to) {
    const cacheKey = `${text}-${from}-${to}`;
    
    if (translationCache.has(cacheKey)) {
        return translationCache.get(cacheKey);
    }
    
    const translation = to !== "tr" 
        ? translations[from][to][text] || text
        : getKeyByValue(translations[to][from], text) || text;
        
    translationCache.set(cacheKey, translation);
    return translation;
}

/**
 * Finds a key in an object by its value
 * @param {Object} object - Object to search in
 * @param {*} value - Value to search for
 * @returns {string|undefined} - Key if found, undefined otherwise
 */
function getKeyByValue(object, value) {
    return Object.keys(object).find(key => object[key] === value);
}

/**
 * Translates a single DOM element's content
 * @param {HTMLElement} element - Element to translate
 * @param {string} to - Target language code
 */
function translateElement(element, to) {
    element.innerHTML = translate(element.textContent.trim(), document.documentElement.lang, to);
}

/**
 * Translates multiple DOM elements
 * @param {HTMLElement[]} elements - Array of elements to translate
 * @param {string} to - Target language code
 */
function translateElements(elements, to) {
    elements.forEach(element => translateElement(element, to));
}

/**
 * Automatically translates all elements with class "translate"
 * @param {string} to - Target language code
 */
function autoTranslateElements(to) {
    document.querySelectorAll(".translate").forEach(element => translateElement(element, to));
}
